package ControlFlow;

public class Loops {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++)
            System.out.println("Hello World! " + i);

        int j = 5;
        while ( j > 0 ){
            System.out.println("while " + j);
            j--;
        }
    }
}
